    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = jTextField1.getText();
        String direccion = jTextField2.getText();
        String telefono = jTextField3.getText();

        EmpleadoPojo empleado = new EmpleadoPojo();
        empleado.setNombre(nombre);
        empleado.setDireccion(direccion);
        empleado.setTelefono(telefono);

        EmpleadoPojo guardado = empleadoDAO.guardar(empleado);

        if (guardado != null) {
            JOptionPane.showMessageDialog(this, "Empleado guardado exitosamente.");
            actualizarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar el empleado.");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        Long id = Long.parseLong(JOptionPane.showInputDialog(this, "Ingrese el ID del empleado a eliminar:"));

        boolean eliminado = empleadoDAO.eliminar(id);

        if (eliminado) {
            JOptionPane.showMessageDialog(this, "Empleado eliminado correctamente.");
            actualizarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Empleado no encontrado.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarIDActionPerformed
        // TODO add your handling code here:
        Long id = Long.parseLong(JOptionPane.showInputDialog(this, "Ingrese el ID del empleado a buscar:"));

        EmpleadoPojo empleado = empleadoDAO.buscarById(id);

        if (empleado != null) {
            jTextField1.setText(empleado.getNombre());
            jTextField2.setText(empleado.getDireccion());
            jTextField3.setText(empleado.getTelefono());
        } else {
            JOptionPane.showMessageDialog(this, "Empleado no encontrado.");
        }
    }//GEN-LAST:event_btnBuscarIDActionPerformed

