    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        Long id = Long.parseLong(JOptionPane.showInputDialog(this, "Ingrese el ID del empleado a modificar:"));
        String nombre = jTextField1.getText();
        String direccion = jTextField2.getText();
        String telefono = jTextField3.getText();

        EmpleadoPojo empleado = new EmpleadoPojo();
        empleado.setNombre(nombre);
        empleado.setDireccion(direccion);
        empleado.setTelefono(telefono);

        EmpleadoPojo modificado = empleadoDAO.modificar(empleado, id);

        if (modificado != null) {
            JOptionPane.showMessageDialog(this, "Empleado modificado correctamente.");
            actualizarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Empleado no encontrado.");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void actualizarTabla() {
        List<EmpleadoPojo> empleados = empleadoDAO.buscarAll();

        String[] columnas = {"ID", "Nombre", "Dirección", "Teléfono"};
        Object[][] datos = new Object[empleados.size()][4];

        for (int i = 0; i < empleados.size(); i++) {
            EmpleadoPojo emp = empleados.get(i);
            datos[i][0] = emp.getClave();
            datos[i][1] = emp.getNombre();
            datos[i][2] = emp.getDireccion();
            datos[i][3] = emp.getTelefono();
        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(datos, columnas));
    }

